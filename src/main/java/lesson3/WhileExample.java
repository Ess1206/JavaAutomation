public class WhileExample {

    public static void main(String[] args) {
        int i = 1;

        while(i < 5){
            System.out.println(i + " ");

            i++;
        }
       int ii=1;
        do{
            ii++;

            System.out.println(ii + " ");
        }while(ii < 5);


    }


}
